    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Usuarios users = new Admin(user.getText(),pass.getText());
        if(contador==0){
            JOptionPane.showMessageDialog(this, "Seleccionar tipo de Usuario! ");
        }
        
        
        if(user.getText().isEmpty() || nombreCompleto.getText().isEmpty() || pass.getText().isEmpty() || edad.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Favor rellenar todas las casillas! ");
        }else if(users.buscar(user.getText(),0)!= -1){
            JOptionPane.showMessageDialog(this, "Este usuario ya existe! ");
        }else{
            edadT=Integer.parseInt(edad.getText());
            JOptionPane.showMessageDialog(this, "Usuario creado correctamente! ");
            Usuarios.usernames.add(users);
            int posicion=users.buscar(user.getText(),0);
            if(tipoDeDatos.equals("admin")){
                Usuarios.usernames.get(posicion).setAcceso("admin");
            }else if(tipoDeDatos.equals("contenido")){
                Usuarios.usernames.get(posicion).setAcceso("contenido");
            }else{
                Usuarios.usernames.get(posicion).setAcceso("limitado");
            }
            user.setText("");
            nombreCompleto.setText("");
            pass.setText("");
            edad.setText("");
            contador=0;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

